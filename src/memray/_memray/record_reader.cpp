bool
RecordReader::processContextSwitch(thread_id_t tid)
{
    d_last.thread_id = tid;
    auto [it, inserted] = d_stack_traces.emplace(tid, stack_t{});
    auto& stack = it->second;
    d_curr_thread_stack = &stack;
    if (inserted) {
        stack.reserve(1024);
    }
    return true;
}

